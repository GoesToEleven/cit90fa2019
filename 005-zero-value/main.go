package main

import "fmt"

func main() {
	var w bool
	// fmt.Println(w)
	fmt.Printf("w stores the VALUE %v and is of TYPE %T \n", w, w)

	var x int
	// fmt.Println(x)
	fmt.Printf("x stores the VALUE %v and is of TYPE %T \n", x, x)

	var y string
	// fmt.Println(y)
	fmt.Printf("y stores the VALUE %v and is of TYPE %T \n", y, y)

	var z float64
	// fmt.Println(z)
	fmt.Printf("z stores the VALUE %v and is of TYPE %T \n", z, z)
}
